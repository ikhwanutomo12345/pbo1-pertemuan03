package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=3;
        switch(jalanJalan){
            case 1:
                System.out.println("urang lapas");
                break;
            case 2:
                System.out.println("urang gila");
                break;
            case 3:
                System.out.println("ubat nyamuk");
                break;
            case 4:
                System.out.println("dubil date");
                break;
                default:
                    System.out.println("mahluk halus");
                    break;
        }
    }
}
